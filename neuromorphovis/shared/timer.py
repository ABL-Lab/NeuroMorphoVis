# System imports
import time


####################################################################################################
# @Timer
####################################################################################################
class Timer:

    ################################################################################################
    # @__init__
    ################################################################################################
    def __init__(self):
        """
        Constructor
        """
        self.starting_time = 0.0
        self.ending_time = 0.0

    ################################################################################################
    # @start
    ################################################################################################
    def start(self):
        """
        Starts the timer.
        """
        self.starting_time = time.time()

    ################################################################################################
    # @end
    ################################################################################################
    def end(self):
        """
        Ends the timer.
        """
        self.ending_time = time.time()

    ################################################################################################
    # @duration
    ################################################################################################
    def duration(self):
        """
        Gets the duration
        """
        return self.ending_time - self.starting_time